// Each sprite is an 18×14 pixel grid drawn as 8×8px rectangles.
// Colors: O=#ff5500 (brand), D=#cc4400 (shadow), W=#fff (white/glass), K=#111 (black), G=#888 (gray), C=#60c0ff (cyan)

type Color = 'O' | 'D' | 'W' | 'K' | 'G' | 'C' | 'Y' | '.';
const PALETTE: Record<string, string> = {
  O: '#ff5500', D: '#cc4400', W: '#ffffff', K: '#111111',
  G: '#888888', C: '#60c0ff', Y: '#ffcc00',
};

function PixelGrid({ grid, size = 8 }: { grid: Color[][], size?: number }) {
  const rects: React.ReactElement[] = [];
  grid.forEach((row, r) =>
    row.forEach((col, c) => {
      if (col === '.') return;
      rects.push(
        <rect key={`${r}-${c}`} x={c * size} y={r * size} width={size} height={size} fill={PALETTE[col]} />
      );
    })
  );
  const w = (grid[0]?.length ?? 0) * size;
  const h = grid.length * size;
  return (
    <svg viewBox={`0 0 ${w} ${h}`} xmlns="http://www.w3.org/2000/svg" className="w-full h-full" style={{ imageRendering: 'pixelated' }}>
      {rects}
    </svg>
  );
}

